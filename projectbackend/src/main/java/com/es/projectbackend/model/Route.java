package com.es.projectbackend.model;

public class Route {
	private String id; // Route identifier
	private String name; // Name of the Route
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
